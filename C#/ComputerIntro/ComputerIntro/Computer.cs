using System;
using System.Collections.Generic;
using System.Text;

namespace ComputerIntro
{
    class Computer
    {
        private String name;
        private String oSystem;
        private String ipAdress;

        public String Osystem
        {
            get { return oSystem; }
            set { oSystem = value; }
        }

        public String IpAdress
        {
            get { return ipAdress; }
            set { ipAdress = value; }
        }

        public String Name
        {
            get { return name; }
            set { name = value; }
        }





    }
}
